public class Persegi {
    
    int keliling(int sisi){
        return (sisi * sisi * sisi * sisi);
    }
    
    double keliling(double sisi){
        return (sisi * sisi * sisi * sisi);
    }
    
    int luas(int sisi){
        return (sisi * sisi);
    }
    
    double luas(double sisi){
        return (sisi * sisi);
    }
}
